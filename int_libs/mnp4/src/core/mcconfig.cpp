#include "libmnp4/core/mcconfig.h"

MCConfig* MCConfig::instance_=NULL;

MCConfig::MCConfig()
{
}

/**
 * Leállítja az osztályt. Szükséges, ha új
 * konfiguráció providert akarsz használni.
 */
void MCConfig::shutdown()
{
    delete instance_;
}

/**
 * Inicializálja az osztályt
 *
 * \param cfg Innen fog jönni a konfiguráció.
 */
void MCConfig::init(QVariant(*cfg)(QString))
{
    Q_ASSERT(!instance_);
    instance_=new MCConfig;
    instance_->cfg_=cfg;
}

/**
 * \return Az osztály egyetlen objektuma.
 */
MCConfig* MCConfig::instance()
{
    Q_ASSERT(instance_);
    return instance_;
}

/**
 * Konfigurációs értékek lekérését végzi.
 *
 * \param A kívánt konfigurációs érték elérési útja
 * \return Az érték, QVariant-ba csomagolva.
 */
QVariant MCConfig::query(QString name)
{
    return (*instance()->cfg_)(name);
}
